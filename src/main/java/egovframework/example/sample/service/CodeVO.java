package egovframework.example.sample.service;

public class CodeVO {
	
	private int code;
	
	private int gid;
	
	private String name; // (gid 상세 정보)

	public int getCode() {
		return code;
	}

	public void setCode(int code) {
		this.code = code;
	}

	public int getGid() {
		return gid;
	}

	public void setGid(int gid) {
		this.gid = gid;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

}
